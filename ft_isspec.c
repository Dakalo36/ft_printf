#include "ft_printf.h"

int	ft_isspec(char c)
{
	if (c == 's' || c == 'S' || c == 'd' || c == 'D' || c == 'i' ||
			c == 'o' || c == 'O' || c == 'u' || c == 'U' || c == 'x' ||
			c == 'X' || c == 'c' || c == 'C' || c == 'p')
		return (1);
	else
		return (0);
}
